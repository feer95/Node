class Book 
{
  constructor(title, genre, author, price, imageUrl, id_book, id_user) 
  {
    this.title = title;
    this.genre = genre;
    this.author = author;
    this.price = price;
    this.imageUrl = imageUrl;
    this.id_book = id_book;
    this.id_user = id_user;
  }
}
module.exports = Book;
  
  